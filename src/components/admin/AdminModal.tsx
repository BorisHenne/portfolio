import { memo, useState, useCallback } from 'react';
import { motion, AnimatePresence } from 'framer-motion';
import { useGoogleLogin } from '@react-oauth/google';
import { X, Shield, LogOut, Upload, AlertCircle } from 'lucide-react';

import { useAuthStore } from '../../stores/useStore';
import { cn } from '../../utils';

// Email autorisé pour l'admin
const ADMIN_EMAIL = import.meta.env.VITE_ADMIN_EMAIL || 'boris.henne@gmail.com';

function AdminModalComponent() {
  const { isAuthenticated, user, setAuthenticated, logout } = useAuthStore();
  const [isOpen, setIsOpen] = useState(false);
  const [error, setError] = useState<string | null>(null);
  const [isLoading, setIsLoading] = useState(false);

  const handleGoogleLogin = useGoogleLogin({
    onSuccess: async (tokenResponse) => {
      setIsLoading(true);
      setError(null);

      try {
        // Fetch user info from Google
        const res = await fetch('https://www.googleapis.com/oauth2/v3/userinfo', {
          headers: { Authorization: `Bearer ${tokenResponse.access_token}` },
        });
        const userData = await res.json();

        // Verify email
        if (userData.email === ADMIN_EMAIL) {
          setAuthenticated(true, {
            email: userData.email,
            name: userData.name,
            picture: userData.picture,
          });
        } else {
          setError(`Accès refusé. Seul ${ADMIN_EMAIL} peut se connecter.`);
        }
      } catch {
        setError('Erreur lors de la connexion');
      } finally {
        setIsLoading(false);
      }
    },
    onError: () => {
      setError('Erreur Google OAuth');
    },
  });

  const handleLogout = useCallback(() => {
    logout();
    setIsOpen(false);
  }, [logout]);

  const toggleModal = useCallback(() => {
    setIsOpen((prev) => !prev);
    setError(null);
  }, []);

  // Admin trigger button (bottom left, discrete)
  const AdminTrigger = (
    <button
      onClick={toggleModal}
      className={cn(
        'fixed bottom-6 left-6 z-40 p-2 rounded-lg transition-all',
        isAuthenticated
          ? 'bg-primary-500/20 text-primary-400'
          : 'bg-dark-800/50 text-gray-600 hover:text-gray-400'
      )}
      aria-label="Administration"
    >
      <Shield size={20} />
    </button>
  );

  return (
    <>
      {AdminTrigger}

      <AnimatePresence>
        {isOpen && (
          <>
            {/* Backdrop */}
            <motion.div
              initial={{ opacity: 0 }}
              animate={{ opacity: 1 }}
              exit={{ opacity: 0 }}
              onClick={toggleModal}
              className="fixed inset-0 z-50 bg-dark-950/80 backdrop-blur-sm"
            />

            {/* Modal */}
            <motion.div
              initial={{ opacity: 0, scale: 0.95 }}
              animate={{ opacity: 1, scale: 1 }}
              exit={{ opacity: 0, scale: 0.95 }}
              className="fixed top-1/2 left-1/2 -translate-x-1/2 -translate-y-1/2 z-50 w-full max-w-md"
            >
              <div className="glass-card rounded-2xl p-6 m-4">
                {/* Header */}
                <div className="flex items-center justify-between mb-6">
                  <h2 className="font-display text-xl font-semibold text-white flex items-center gap-2">
                    <Shield className="text-primary-500" size={24} />
                    Administration
                  </h2>
                  <button
                    onClick={toggleModal}
                    className="p-2 rounded-lg text-gray-400 hover:text-white hover:bg-white/5 transition-all"
                  >
                    <X size={20} />
                  </button>
                </div>

                {/* Content */}
                {isAuthenticated && user ? (
                  <div className="space-y-6">
                    {/* User info */}
                    <div className="flex items-center gap-4 p-4 rounded-xl bg-primary-500/10 border border-primary-500/20">
                      {user.picture && (
                        <img
                          src={user.picture}
                          alt={user.name}
                          className="w-12 h-12 rounded-full"
                        />
                      )}
                      <div>
                        <p className="font-medium text-white">{user.name}</p>
                        <p className="text-sm text-gray-400">{user.email}</p>
                      </div>
                    </div>

                    {/* Admin actions */}
                    <div className="space-y-3">
                      <button className="btn-secondary w-full flex items-center justify-center gap-2">
                        <Upload size={18} />
                        Mettre à jour le CV
                      </button>
                    </div>

                    {/* Logout */}
                    <button
                      onClick={handleLogout}
                      className="w-full flex items-center justify-center gap-2 px-4 py-3 rounded-xl text-red-400 hover:bg-red-500/10 transition-colors"
                    >
                      <LogOut size={18} />
                      Déconnexion
                    </button>
                  </div>
                ) : (
                  <div className="space-y-6">
                    <p className="text-gray-400 text-center">
                      Connectez-vous pour accéder aux fonctionnalités d'administration.
                    </p>

                    {/* Error message */}
                    {error && (
                      <div className="p-3 rounded-lg bg-red-500/20 border border-red-500/30 flex items-start gap-2 text-red-400 text-sm">
                        <AlertCircle size={16} className="mt-0.5 flex-shrink-0" />
                        <span>{error}</span>
                      </div>
                    )}

                    {/* Google login button */}
                    <button
                      onClick={() => handleGoogleLogin()}
                      disabled={isLoading}
                      className="btn-primary w-full flex items-center justify-center gap-3"
                    >
                      {isLoading ? (
                        <div className="w-5 h-5 border-2 border-dark-950/30 border-t-dark-950 rounded-full animate-spin" />
                      ) : (
                        <>
                          <svg className="w-5 h-5" viewBox="0 0 24 24">
                            <path
                              fill="currentColor"
                              d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"
                            />
                            <path
                              fill="currentColor"
                              d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"
                            />
                            <path
                              fill="currentColor"
                              d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"
                            />
                            <path
                              fill="currentColor"
                              d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"
                            />
                          </svg>
                          Connexion avec Google
                        </>
                      )}
                    </button>

                    <p className="text-gray-600 text-xs text-center">
                      Seul <span className="text-primary-400">{ADMIN_EMAIL}</span> peut se connecter.
                    </p>
                  </div>
                )}
              </div>
            </motion.div>
          </>
        )}
      </AnimatePresence>
    </>
  );
}

export default memo(AdminModalComponent);
